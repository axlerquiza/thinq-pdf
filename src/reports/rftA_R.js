const PDFDocument = require("pdfkit-table");

exports.generatePDF = function (connection, username, res) {
    // Create a new PDF document
    const doc = new PDFDocument({
        info: {
            Title: 'Ratings on Fulfilled Tickets Report', //document title
            Author: username,
            Subject: 'All Time System Reporting',
            CreationDate: new Date()
        },
        margin: 30,
        size:'Letter'
    });

    // Use a prepared statement with placeholders
    const query = 'SELECT * FROM FORM_TABLE WHERE STATUS = ? ORDER BY DATE_SUBMITTED';
    const values = ['CLOSED'];

    connection.query(query, values, (err, results) => {
        if (err) throw err;

        const tableHeaders = ['Date Issued', 'Concern', 'Subconcern', 'Fulfillment Rating'];

        const tableRows = results.map((row) => [
            row.date_submitted,
            row.concern,
            row.subconcern,
            row.satisfaction_answer
        ]);

        const table = {
            title: "Ratings on Fulfilled Tickets Report",
            subtitle: "All Time System Reporting",
            headers: [
                { label: tableHeaders[0], align: "center", },
                { label: tableHeaders[1], align: "center", },
                { label: tableHeaders[2], align: "center", },
                { label: tableHeaders[3], align: "center", },
            ],
            // simple data
            rows: [],
        };

        for (let i = 0; i < results.length; i++) {
            const row = results[i];
            const tableRow = [row.date_submitted.toLocaleDateString("en-US"), row.concern, row.subconcern, row.satisfaction_answer];
            table.rows.push(tableRow);
        }

        // the magic
        doc.table(table, {
            prepareHeader: () => doc.font("Times-Bold").fontSize(12),
            prepareRow: (row, indexColumn, indexRow, rectRow, rectCell, i) => {
                doc.font("Times-Roman").fontSize(12);
                indexColumn === 0 && doc.addBackground(rectRow, 'white', 0.15);
            },
        });
        const today = new Date();
        const datetime = today.toLocaleString();
        doc.text("Report generated by: " + username + " on " + datetime, {
            align: 'left'
        });

        // Set the appropriate headers for the file download
        res.setHeader('Content-Type', 'application/pdf');
        res.setHeader('Content-Disposition', 'attachment; filename=ticket-ratings-alltime_report.pdf');

        // Pipe the PDF document to the response
        doc.pipe(res);

        // Finalize the PDF document
        doc.end();

    });

}